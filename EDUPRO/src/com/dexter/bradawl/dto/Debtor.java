package com.dexter.bradawl.dto;

import java.util.ArrayList;

public class Debtor
{
	private int student_id;
	private String student_nm;
	
	private double total_debt;
	private ArrayList<String[]> debts;
	
	public Debtor()
	{}

	public int getStudent_id() {
		return student_id;
	}

	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}

	public String getStudent_nm() {
		return student_nm;
	}

	public void setStudent_nm(String student_nm) {
		this.student_nm = student_nm;
	}

	public double getTotal_debt() {
		return total_debt;
	}

	public void setTotal_debt(double total_debt) {
		this.total_debt = total_debt;
	}

	public ArrayList<String[]> getDebts() {
		if(debts == null)
			debts = new ArrayList<String[]>();
		return debts;
	}

	public void setDebts(ArrayList<String[]> debts) {
		this.debts = debts;
	}
	
}
