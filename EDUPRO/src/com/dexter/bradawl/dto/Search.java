package com.dexter.bradawl.dto;

public class Search
{
	private int session_id;
	private int term_id;
	private int student_id;
	private int class_id;
	private String class_lvl;
	private int lvl_no;
	private int fee_cat_id;
	
	private int sub_id;
	
	public Search()
	{}

	public int getSession_id() {
		return session_id;
	}

	public void setSession_id(int session_id) {
		this.session_id = session_id;
	}

	public int getTerm_id() {
		return term_id;
	}

	public void setTerm_id(int term_id) {
		this.term_id = term_id;
	}

	public int getStudent_id() {
		return student_id;
	}

	public void setStudent_id(int student_id) {
		this.student_id = student_id;
	}

	public int getClass_id() {
		return class_id;
	}

	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}

	public String getClass_lvl() {
		return class_lvl;
	}

	public void setClass_lvl(String class_lvl) {
		this.class_lvl = class_lvl;
	}

	public int getLvl_no() {
		return lvl_no;
	}

	public void setLvl_no(int lvl_no) {
		this.lvl_no = lvl_no;
	}

	public int getFee_cat_id() {
		return fee_cat_id;
	}

	public void setFee_cat_id(int fee_cat_id) {
		this.fee_cat_id = fee_cat_id;
	}

	public int getSub_id() {
		return sub_id;
	}

	public void setSub_id(int sub_id) {
		this.sub_id = sub_id;
	}
	
}
