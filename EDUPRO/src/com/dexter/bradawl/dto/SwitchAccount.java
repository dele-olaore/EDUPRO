package com.dexter.bradawl.dto;

import java.util.Date;

public class SwitchAccount
{
	private int id;
	private int switch_id;
	private String switch_nm;
	private String account_number;
	private String other_details;
	private Date crt_dt;
	
	public SwitchAccount()
	{}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSwitch_id() {
		return switch_id;
	}

	public void setSwitch_id(int switch_id) {
		this.switch_id = switch_id;
	}

	public String getSwitch_nm() {
		return switch_nm;
	}

	public void setSwitch_nm(String switch_nm) {
		this.switch_nm = switch_nm;
	}

	public String getAccount_number() {
		return account_number;
	}

	public void setAccount_number(String account_number) {
		this.account_number = account_number;
	}

	public String getOther_details() {
		return other_details;
	}

	public void setOther_details(String other_details) {
		this.other_details = other_details;
	}

	public Date getCrt_dt() {
		return crt_dt;
	}

	public void setCrt_dt(Date crt_dt) {
		this.crt_dt = crt_dt;
	}
	
}
